// 5.Create a variable that is a number. 
// Use an if/else statement to log whether the number is bigger than, smaller than or equal to 10.
let number = 6;
if (number > 10) {
    return console.log(`${number} is greater than 10.`);
} else if (number < 10) {
    return console.log(`${number} is less than 10.`);
} else (number == 10) 
    return console.log(`${number} is equal to 10.`);
